/// Look up the vendor/manufacturer for a given MAC address using IEEE OUI prefix.
pub fn lookup_vendor(mac: &MacAddr) -> Option<&'static str> {
    let db = OUI_DB.get_or_init(build_oui_db);
    let prefix = [mac.0, mac.1, mac.2];
    db.get(&prefix).copied()
}

